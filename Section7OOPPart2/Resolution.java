package Section7OOPPart2;

public class Resolution {
    private int widht;
    private int height;

    public Resolution(int widht, int height) {
        this.widht = widht;
        this.height = height;
    }

    public int getWidht() {
        return widht;
    }

    public int getHeight() {
        return height;
    }
}
